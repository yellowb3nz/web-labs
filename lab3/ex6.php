// 2.3 Корень из суммы квадратов элементов массива
$array = [4, 2, 5, 19, 13, 0, 10];
$sumOfSquares = 0;
foreach ($array as $number) {
    $sumOfSquares += $number * $number;
}
$result = sqrt($sumOfSquares);
echo "2.3 Корень из суммы квадратов массива: $result\n";

