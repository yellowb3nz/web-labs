echo "  До сотых: " . round($sqrt, 2) . "\n";

// 3.2 Округление √587
$sqrt = sqrt(587);
$rounded = [
    'floor' => floor($sqrt),
    'ceil' => ceil($sqrt)
];
echo "3.2 Округление √587:\n";
print_r($rounded);

