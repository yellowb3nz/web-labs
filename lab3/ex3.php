<?php
// 1) Количество языков и месяцев обучения
$numLanguages = 4; // Ruby, Python, JavaScript, C++
$months = 11;      // Всего месяцев обучения

// 2) Расчет общего количества дней обучения
$days = $months * 16; // 16 дней в месяц
echo "Общее количество дней обучения: " . $days . "\n";

// 3) Расчет среднего количества дней на один язык
$daysPerLanguage = $days / $numLanguages;
echo "Среднее количество дней на изучение одного языка: " . $daysPerLanguage;