<?php
// 1. Функция, которая печатает строку и возвращает число
function printStringReturnNumber() {
    echo "Эта строка выводится функцией\n";
    return 42; // Возвращаем числовое значение
}

// 2. Записываем возвращаемое значение в переменную
$my_num = printStringReturnNumber();

// 3. Выводим значение переменной
echo "Возвращенное число: $my_num\n";
?>
