<?php
// 1) Создаем переменную с любым числом
$myNum = 1337; // Можно выбрать любое число

// 2) Присваиваем значение $myNum переменной $answer
$answer = $myNum;

// 3) Добавляем 2 к $answer (оператор +=)
$answer += 2;

// 4) Умножаем $answer на 2 (оператор *=)
$answer *= 2;

// 5) Вычитаем 2 из $answer (оператор -=)
$answer -= 2;

// 6) Делим $answer на 2 (оператор /=)
$answer /= 2;

// 7) Вычитаем исходное число ($myNum) из $answer
$answer -= $myNum;

// 8) Выводим результат (всегда должен быть 1)
echo $answer;